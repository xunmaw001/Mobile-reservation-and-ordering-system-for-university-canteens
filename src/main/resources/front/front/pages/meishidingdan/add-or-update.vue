					let res = await this.$api.list(`meishidingdan`, params);
					if (res.data.total >= crossoptnum) {
						this.$utils.msg(uni.getStorageSync('tips'));
						return false;
					} else {
                //跨表计算
                        var obj = uni.getStorageSync('crossObj');
                        var table = uni.getStorageSync('crossTable');
                        obj.shuliang = parseFloat(obj.shuliang) - parseFloat(this.ruleForm.shuliang)
                        await this.$api.update(`${table}`, obj);
						if(this.ruleForm.id){
							await this.$api.update(`meishidingdan`, this.ruleForm);
						}else{
							await this.$api.add(`meishidingdan`, this.ruleForm);
						}
						this.$utils.msgBack('提交成功');
